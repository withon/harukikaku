/**
 * 聚焦栏和视频栏，首次加载时及页面大小调整时，更改样式
 */
$(window).on("load resize", function () {
    // 保持聚焦栏的大小与页面相同，使背景图片大小填充70%屏幕
    if ($(".spotlight").length) {
      $(".spotlight").css({
        height: Math.max($(window).width() * 0.3, 220) + "px",
      });
    }
  });
  